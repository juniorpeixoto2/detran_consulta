              Saiba o que é uma carteira de investimentos
            </h3>
            <p>Uma carteira de investimentos é algo essencial para quem quer começar a investir. Também chamada de cesta ou portfólio de investimentos, é nela que estarão reunidos todos os ativos financeiros que você deve definir para fazer o seu dinheiro render.</p>
            <p>Todo investidor precisa estar ciente da importância de uma boa carteira de investimentos. Lembramos que cada carteira é única, ou seja, vai depender exclusivamente de cada investidor, sendo algo importante comum a todos: é preciso haver certa diversificação para diminuir riscos e ampliar potenciais de ganho.</p>
            <p>Quando você diversifica, ou seja, reparte o capital em mais ativos, consegue maior segurança financeira, uma vez que, se estiver perdendo em um deles, pode recuperar e ganhar mais em outro, e vice-versa. Riscos sempre farão parte de um investimento, mas é possível balançear a carteira para diminui-los ao máximo.</p>
            <p>Também é importante estar sempre de olho em pontos como a sua situação e necessidades atuais e as suas perspectivas para o curto, médio e longo prazos. Pode ser que em determinado momento da vida você esteja mais disposto a correr riscos e, assim, alocar um percentual maior dos investimentos em renda variável por exemplo.</p>
            <p>Em outras épocas, pode ser que você precise necessariamente de segurança e liquidez, por isso é precisso alocar a maior parte da carteira em opções de investimento que contenham estes dois fatores embutidos.</p>
