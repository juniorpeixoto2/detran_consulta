              Conheça seus objetivos e ciclos de investimento
            </h3>
            <p>Conforme falei anteriormente, é importante que você conheça seus objetivos de vida e ciclos de cada potencial investimento. Ou seja, é preciso entender em qual momento de vida você está. Muitas vezes estamos uma fase de acúmulo de patrimônio, por exemplo, quando tudo que queremos é conseguir mais dinheiro e mais bens. Normalmente esta fase acontece quando somos mais jovens e pensamos com uma visão de longo prazo. Neste caso dá até para arriscar mais, entende? E a carteira pode contar com investimentos que oscilem um pouco mais e tragam maiores possibilidades de ganho aliadas, obviamente, a maior risco de perda.</p>
            <p>Por outro lado, se estivermos em uma fase de preservação do patrimônio, provavelmente vamos priorizar a segurança em detrimento dos ganhos financeiros. Desta forma, a carteira terá que espelhar esta fase da vida e os rendimentos das aplicações provavelmente servirão para complementar a renda somente. O patrimônio já está consolidado. Consegue ver como, de acordo com cada fase, a carteira muda?</p>
            <p>De forma resumida, podemos separar os ciclos financeiros da vida em três fases e é necessário que você entenda em qual deles está antes de formar a sua carteira. São eles:</p>
