          <div class="mt-5">
            <h3 class="mb-3">
              Defina seu perfil de investidor
            </h3>
            <p>Agora é hora de falar sobre seu perfil de investidor. É ele que ajudará a definir se está disposto a correr mais ou menos risco nos investimentos. Separamos três dos principais perfis, mas considere que pode haver quem penda mais para um lado ou outro. Seja sincero em sua auto-análise, combinado?</p>
            <p>Uma pessoa conservadora, por exemplo, sempre tenderá a preferir investimentos de renda fixa, pois afirma que não há possibilidade de perder dinheiro com estas aplicações. Não está considerando, porém, que apesar de não “perder” dinheiro, também não estará ganhando mais com outras opções. Na prática, realmente pode estar perdendo dinheiro sem saber!</p>
            <p>Há também quem seja moderado, e busque tirar vantagem de todas as possibilidades, aceitando tomar um pouco de risco, mas diversificando a carteira para proteger a maior parte dos ativos.</p>
            <p>E é claro, há os arrojados ou experientes, que buscam retornos acima da média e não se importam com oscilações de curto prazo, pois sabem lidar melhor com as perdas e riscos.</p>
            <p>Somado à uma análise dos ciclos financeiros em que você se encontra, conhecer o seu perfil será fundamental para montar uma carteira de investimentos de acordo com o que você deseja e precisa. Vamos lá?</p>
          </div>
          <div class="mt-5">
            <h3 class="mb-3">
              Saiba um pouco mais sobre cada um dos perfis
            </h3>
            <p>Conservador: Não aceita volatilidade em sua carteira, procura o máximo de segurança e previsibilidade em seus investimentos, por isso costuma investir quase tudo em renda fixa. Não costuma investir em ações por conta da volatilidade. E caso resolva diversificar, provavelmente vai atrás dos fundos de renda fixa ou multimercados de baixo risco. E mesmo que deixe de ganhar, sua principal intenção é não perder o que já tem.
            </p>
            <p>
              Moderado: Aceita e entende o conceito da volatilidade, busca ganhos acima da média de mercado para médio e longo prazo. Este perfil tem uma mínima exposição ao risco no curto prazo, contanto que seus objetivos estejam garantidos no médio e no longo prazo. Quer uma geração de renda frequente e previsível, que tenha pouca volatilidade. Pode investir uma parte do portfólio em ações ou opções e o restante, a maior parte provavelmente, em renda fixa.</p>
            <p>Experiente: Busca retornos acima da média de mercado para multiplicação de patrimônio. Não se importa com oscilações negativas no curto e médio prazo. Tolera a volatilidade da renda variável, pois sabe que, no fim, seus objetivos de valorização poderão ser alcançados. Normalmente, investe a menor parte do portfólio em renda fixa.</p>
          </div>
          <div class="mt-5">
