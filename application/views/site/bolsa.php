              Entenda os prazos de investimento
            </h3>
            <p>Agora que você já entende como funciona uma carteira de ações, já definiu seus ciclos de vida e seus ciclos financeiros e também já conhece seu perfil de investidor, é importante entender os prazos de investimento.
            </p>
            <p>
              Se você precisará de dinheiro no curto prazo por exemplo, não pode manter a maior parte de sua carteira em investimentos de longo prazo, entende? E se está investindo visando ao longo prazo, como uma reserva para a aposentadoria por exemplo, não há porquê colocar os recursos em maior parte em curto prazo.
            </p>
            <p>
              Os prazos normalmente são definidos assim:
            </p>
            <p class="mb-2">
              Curto: De 1 a 6 meses
            </p>
            <p class="mb-2">
              Médio: De 6 meses a 2 anos
            </p>
            <p class="mb-2">
              Longo: De 2 anos em diante
            </p>
            <p>É preciso entender também que prazo e volatilidade normalmente são diretamente proporcionais. O que isso significa? Que quanto menor o prazo, é menos recomendável tomar risco de mercado. Já quando o prazo é maior, o risco pode ser mais facilmente absorvido na carteira ao longo do tempo de aplicação, portanto você pode arriscar um pouco mais para ganhar mais também.</p>
          </div>
          <div class="mt-5">
            <h3 class="mb-3">
              Lembre-se de alguns pontos
            </h3>
            <p>
              • Quem está começando a investir e tem o curto prazo como objetivo, deve dar preferência para ativos de renda fixa, como Títulos do Tesouro, CDBs, LCIs, e LCAs. Neste caso é preciso checar liquidez e carência também, afinal, pode haver necessidade de resgate logo.
            </p>
            <p>
              • Quem está focando no médio prazo pode arriscar um pouco mais, por isso, além dos ativos de renda fixa que devem formar a maior parte da carteira, vale a pena considerar um percentual menor de renda variável.</p>
            <p>• Quem tem o longo prazo como foco ganha tempo para investir de forma muito mais diversificada, pois pode recuperar eventuais perdas e arriscar um pouco mais. Neste caso, vale além das opções de renda fixa com prazos maiores (10 ou 20 anos por exemplo), também dá para pensar em ações de grandes empresas e outras alternativas.
            </p>
          </div>
