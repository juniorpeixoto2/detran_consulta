              Aplique alguns filtros para fazer a escolha certa
            </h3>
            <p>
              Agora que você já recebeu uma série de informações sobre uma carteira de investimentos e outros pontos relacionados, vamos fazer uma revisão aplicando alguns filtros para tentar diminuir riscos e te ajudar a fazer escolhas mais certeiras, certo? Os filtros são estes:
            </p>
            <p>
              Prazo do investimento: Como explicamos anteriormente, é ele quem determinará a possibilidade de se tomar maior ou menor risco. Em investimentos de longo prazo, é natural que se possa correr um pouco mais de risco, pois há mais tempo para recuperar eventuais perdas.</p>
            <p>Análise do perfil: Você precisa saber se seu perfil é conservador, moderado ou arrojado, pois isso ajudará a entender se está disposto a tomar mais ou menos risco nos investimentos.</p>
